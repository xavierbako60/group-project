import numpy as np

def add_numbers(a, b):
    """
    Add two numbers.
    
    Args:
        a (int): The first number.
        b (int): The second number.
        
    Returns:
        int: The sum of the two numbers.
    """
    return a + b

# Example usage
result = add_numbers(3, 5)
print(result)  # Output: 8
